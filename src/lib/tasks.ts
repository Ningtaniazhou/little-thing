export function getRandomTask(categories?: string[]): Task {
  const pool = categories?.length
    ? LOCAL_TASKS.filter((t) => categories.includes(t.category))
    : LOCAL_TASKS;
  return pool[Math.floor(Math.random() * pool.length)];
}
